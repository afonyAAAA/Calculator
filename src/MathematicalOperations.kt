object MathematicalOperations : Operations {

    override fun multiplication(num1 : Double, num2: Double): Double = num1 * num2

    override fun subtraction(num1: Double, num2: Double): Double = num1 - num2

    override fun division(num1: Double, num2: Double): Double = num1 / num2

    override fun sum(num1: Double, num2: Double): Double = num2 + num2

}